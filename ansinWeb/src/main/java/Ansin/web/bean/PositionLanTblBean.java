package Ansin.web.bean;

public class PositionLanTblBean {

	// 会社ID
    private String companyId;
    // 職位ID
    private String positionId;
    // 言語ID
    private String languageId;
    // 言語レベル区分
    private String levenSention;
	/**
	 * @return the companyId
	 */
	public String getCompanyId() {
		return companyId;
	}
	/**
	 * @param companyId the companyId to set
	 */
	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}
	/**
	 * @return the positionId
	 */
	public String getPositionId() {
		return positionId;
	}
	/**
	 * @param positionId the positionId to set
	 */
	public void setPositionId(String positionId) {
		this.positionId = positionId;
	}
	/**
	 * @return the languageId
	 */
	public String getLanguageId() {
		return languageId;
	}
	/**
	 * @param languageId the languageId to set
	 */
	public void setLanguageId(String languageId) {
		this.languageId = languageId;
	}
	/**
	 * @return the levenSention
	 */
	public String getLevenSention() {
		return levenSention;
	}
	/**
	 * @param levenSention the levenSention to set
	 */
	public void setLevenSention(String levenSention) {
		this.levenSention = levenSention;
	}
    
}
