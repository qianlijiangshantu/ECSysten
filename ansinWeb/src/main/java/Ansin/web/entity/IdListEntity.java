	private Integer trainingId;
	
	private Integer briefingId;
	
	public Integer getPositionId() {
		return positionId;
	}

	public void setPositionId(Integer positionId) {
		this.positionId = positionId;
	}

	public Integer getCompanyId() {
		return companyId;
	}

